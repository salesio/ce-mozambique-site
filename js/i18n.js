// Store original Portuguese text so we can switch back to "pt" without a reload
const originalText = {};
document.querySelectorAll("[data-i18n]").forEach((el) => {
  originalText[el.getAttribute("data-i18n")] = el.innerHTML;
});

function saveLanguage(lang) {
  try {
    localStorage.setItem("ce-language", lang);
  } catch {
    // Some privacy settings disable local storage; language switching should still work.
  }
}

function getSavedLanguage() {
  try {
    return localStorage.getItem("ce-language");
  } catch {
    return null;
  }
}

function applyLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (lang === "pt") {
      el.innerHTML = originalText[key];
    } else if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });
  document.documentElement.lang = lang;
  document.querySelectorAll(".lang-switch button").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });
  saveLanguage(lang);
}

document.querySelectorAll(".lang-switch button").forEach((btn) => {
  btn.addEventListener("click", () => applyLanguage(btn.getAttribute("data-lang")));
});

const savedLanguage = getSavedLanguage();
if (savedLanguage && ["pt", "en", "fr"].includes(savedLanguage)) {
  applyLanguage(savedLanguage);
}

const currentPage = window.location.pathname.split("/").pop() || "index.html";
document.querySelectorAll(".navbar .nav-link").forEach((link) => {
  if (link.getAttribute("href") === currentPage) {
    link.setAttribute("aria-current", "page");
    link.classList.add("active");
  }
});

document.querySelectorAll(".navbar-collapse .nav-link").forEach((link) => {
  link.addEventListener("click", () => {
    const nav = document.querySelector(".navbar-collapse.show");
    if (nav && window.bootstrap) {
      window.bootstrap.Collapse.getOrCreateInstance(nav).hide();
    }
  });
});
